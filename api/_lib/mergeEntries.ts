// ─────────────────────────────────────────────────────────────────────────────
// mergeEntries — shared core for the /api/entries?action=merge web flow,
// the MCP merge_entries tool, the chat merge_entries tool, and the
// /v1/merge public REST endpoint. Everything that touches the merge
// pipeline (LLM prompt, markdown sanitiser, metadata inheritance, quota
// peek, audit-log write, source soft-delete) lives here so the four
// surfaces can't drift.
//
// The web flow is a two-phase preview/commit (user reviews and edits the
// LLM output before committing). The non-interactive flows are one-shot
// (generate + commit in one call) — see mergeEntriesOneShot.
// ─────────────────────────────────────────────────────────────────────────────

